    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
        int bookingId = Integer.parseInt(request.getParameter("id"));
        Bookings booking = bookingsFacade.find(bookingId);
        request.setAttribute("booking", booking);
        request.getRequestDispatcher("/admin/editBooking.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int bookingId = Integer.parseInt(request.getParameter("bookingId"));
        Bookings booking = bookingsFacade.find(bookingId);
        String status = request.getParameter("status");
        booking.setStatus(status);
        bookingsFacade.edit(booking);
         HttpSession session = request.getSession();
        // session.setAttribute("adminId", "admin1");
        response.sendRedirect(request.getContextPath() + "/admin/booking");
    }

